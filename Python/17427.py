'''
만약 n이 9일경우
f(1) = 1
f(2) = 1 + 2
f(3) = 1 + 3
f(4) = 1 + 2 + 4
f(5) = 1 + 5
f(6) = 1 + 2 + 3 + 6
f(7) = 1 + 7
f(8) = 1 + 2 + 4 + 8
f(9) = 1 + 3 + 9

=> 1*9 + 2*4 + 3*3 + 4*2 + 5*1 + 6*1 + 7*1 + 8*1 + 9*1 = 69
"/" = 나누기, "//" =  몫, "%" = 나머지 
=> 1*(9//1) + 2*(9//2) + 3*(9//3) + 4*(9//4) + 5*(9//5) + 6*(9//6) + 7*(9//7) + 8*(9//8) + 9*(9//9) = 69
'''

def main(N):
    ans = 0
    for i in range(1, N+1):
        ans += i * (N//i)
    print(ans)


if __name__ == "__main__":    
    num = int(input())
    main(num)
